
/*
 * Programa que pide un número de mes y muestra el nombre correspondiente
 */
import java.util.*;
public class Ejemplo0Switch {
    public static void main(String[] args) {
        int mes; 
        Scanner sc = new Scanner(System.in);
        System.out.print("Introduzca un numero de mes: ");
        mes = sc.nextInt();
        switch (mes) /*dependiendo del valor que tenga mes, de 1 a 12, ira al correspondiente caso y hara las operaciones que se digan.
		 Si el mes no esta entre 1 y 12 ira a default, que hara otras operaciones, en este caso dira que el mes no es valido. En esta 			estructura solo se ejecuta el caso que se marque sin ir comprobando todas las lineas si se cumple o no alguna condicion*/
        {
                case 1: System.out.println("ENERO");
                           break; //para decir que termina la ejecucion del case, hasta aqui
                case 2: System.out.println("FEBRERO");
                           break;
                case 3: System.out.println("MARZO");
                           break;
                case 4: System.out.println("ABRIL");
                           break;
                case 5: System.out.println("MAYO");
                           break;
                case 6: System.out.println("JUNIO");
                           break;
                case 7: System.out.println("JULIO");
                           break;
                case 8: System.out.println("AGOSTO");
                           break;
                case 9: System.out.println("SEPTIEMBRE");
                           break;
                case 10: System.out.println("OCTUBRE");
                             break;
                case 11: System.out.println("NOVIEMBRE");
                             break;
                case 12: System.out.println("DICIEMBRE");
                             break;
                default : System.out.println("Mes no válido");                       
        }
    }
}

